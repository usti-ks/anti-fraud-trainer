function QuickCheckPage() {
  const { audience } = useAudience();
  const [progress, setProgress] = useSharedProgress();
  const indicators = quickCheckIndicators.filter((item) => item.audiences.includes(audience.id));
  const [selected, setSelected] = useState([]);

  const score = selected.reduce((sum, id) => sum + (indicators.find((item) => item.id === id)?.weight || 0), 0);
  const result = score >= 6 ? 'Высокий риск' : score >= 3 ? 'Средний риск' : selected.length ? 'Низкий риск, но проверка всё равно нужна' : 'Отметьте признаки';

  const explanation =
    score >= 6
      ? 'Ситуация выглядит очень опасной. Контакт лучше прервать и перейти к экстренному или безопасному маршруту.'
      : score >= 3
        ? 'Есть несколько серьёзных признаков. Не спешите и перепроверьте всё через официальный канал.'
        : selected.length
          ? 'Явных признаков мало, но это не означает безопасность. Проверьте источник и не сообщайте чувствительные данные.'
          : 'Отметьте, что происходит в вашей ситуации, чтобы получить оценку.';

  const toggle = (id) => {
    setSelected((current) => (current.includes(id) ? current.filter((item) => item !== id) : [...current, id]));
  };

  const saveCheck = () => {
    if (!selected.length) return;
    setProgress((current) => ({
      ...current,
      quickChecks: [
        {
          audienceId: audience.id,
          score,
          result,
          indicators: selected,
          createdAt: new Date().toISOString(),
        },
        ...current.quickChecks,
      ].slice(0, 12),
    }));
  };

  return (
    <div className="page-stack">
      <SectionTitle
        eyebrow="Быстрый инструмент"
        title={audience.id === "child" ? "Похоже ли это на ловушку?" : "Похоже ли это на мошенничество?"}
        text={audience.id === "child" ? "Отметьте, что происходит: обещают приз, торопят, просят код, экран или телефон взрослого. Справа сразу появится понятный вывод и безопасный следующий ход." : "Отметьте признаки, которые уже появились в вашей ситуации. Оценка меняется сразу, а справа появляется понятное объяснение и следующий безопасный шаг."}
      />

      <div className="layout-2col layout-2col--balanced">
        <article className="card">
          <div className="check-grid">
            {indicators.map((item) => (
              <button key={item.id} type="button" className={`check-item${selected.includes(item.id) ? ' check-item--done' : ''}`} onClick={() => toggle(item.id)}>
                <div className="check-item__header"><strong>{item.label}</strong></div>
                <span className="check-item__meta">Вес риска: {item.weight}</span>
              </button>
            ))}
          </div>
        </article>

        <aside className="stack-list">
          <article className={`card risk-card${score >= 6 ? ' risk-card--high' : score >= 3 ? ' risk-card--medium' : ''}`}>
            <div className="score-pill">{result}</div>
            <p>{explanation}</p>
            <div className="hero__actions">
              <button type="button" className="button" onClick={saveCheck}>Сохранить проверку</button>
              <Link to={score >= 6 ? '/emergency' : '/dialogue-coach'} className="button button--secondary">
                {score >= 6 ? 'Открыть экстренную помощь' : 'Показать безопасный ответ'}
              </Link>
            </div>
          </article>

          <article className="card">
            <h3>Чаще всего в этом режиме встречается</h3>
            <TagList items={audience.topThreats} />
          </article>
        </aside>
      </div>
    </div>
  );
}

