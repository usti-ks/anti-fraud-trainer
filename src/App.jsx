function localAiReply(question, audience) {
  const text = question.toLowerCase();
  const bullets = [];
  let risk = 'Средний риск';
  let nextRoute = '/quick-check';

  if (text.includes('код') || text.includes('sms')) {
    risk = 'Высокий риск';
    bullets.push('Не сообщайте код никому и сразу прекращайте контакт.');
    bullets.push('Проверьте сервис только через официальный сайт или приложение.');
    nextRoute = '/emergency/code';
  }
  if (text.includes('банк') || text.includes('карта')) {
    risk = 'Высокий риск';
    bullets.push('Банк не просит код и не переводит деньги на “безопасный счёт”.');
    bullets.push('Перезвоните по официальному номеру, который нашли сами.');
    nextRoute = '/simulator/bank-call';
  }
  if (text.includes('работ') || text.includes('вакан') || text.includes('подработ')) {
    risk = 'Высокий риск';
    bullets.push('Не оплачивайте доступ, обучение или депозит ради работы.');
    bullets.push('Проверьте работодателя по официальному сайту и реквизитам.');
    nextRoute = '/simulator/job-scam';
  }
  if (text.includes('ссылк') || text.includes('перейти')) {
    risk = 'Высокий риск';
    bullets.push('Не открывайте внешнюю ссылку и не вводите данные на странице.');
    bullets.push('Если уже нажали, переходите в маршрут “Я нажал подозрительную ссылку”.');
    nextRoute = '/emergency/link';
  }
  if (text.includes('прилож') || text.includes('установ')) {
    risk = 'Высокий риск';
    bullets.push('Не устанавливайте приложение по просьбе незнакомого собеседника.');
    bullets.push('Если уже установили — отключите интернет и меняйте пароли с другого устройства.');
    nextRoute = '/emergency/app';
  }
  if (text.includes('приз') || text.includes('игр') || text.includes('бонус')) {
    risk = 'Средний или высокий риск';
    bullets.push('Приз, который требует код, пароль или карту — подозрителен.');
    bullets.push('Сначала покажите ситуацию взрослому или проверьте через официальный источник.');
    nextRoute = '/simulator/prize-link';
  }
  if (text.includes('голос') || text.includes('видео') || text.includes('дипф')) {
    risk = 'Высокий риск';
    bullets.push('Похожий голос не означает подлинность.');
    bullets.push('Обязательна перепроверка через второй канал связи.');
    nextRoute = '/deepfake';
  }

  if (!bullets.length) {
    bullets.push('Опишите, кто пишет или звонит, что просит и торопят ли вас.');
    bullets.push('Пока не сообщайте коды, пароли, данные карты и не переходите по ссылкам.');
  }

  const modeHint = {
    senior: 'Для режима пожилых главный приоритет — прекратить разговор и перепроверить через официальный номер.',
    student: 'Для режима студентов особое внимание уделяйте ссылкам, вакансиям, аренде и внешним формам оплаты.',
    child: 'Для режима детей главный принцип простой: ничего не открывать и сразу показать взрослому.',
    relative: 'Для родственников важнее не только личная реакция, но и спокойная помощь близкому по шагам.',
    general: 'В общем режиме ориентируйтесь на базовые красные флаги: код, деньги, срочность, ссылка, запрет на проверку.',
  }[audience.id];

  return {
    risk,
    text: [`Оценка: ${risk}.`, ...bullets, modeHint, `Откройте следующий маршрут: ${nextRoute}`].join('\n'),
    nextRoute,
  };
}

function AIAssistantPage() {
  const { audience } = useAudience();
  const [messages, setMessages] = useState([
    {
      role: 'assistant',
      content: `Опишите ситуацию: кто пишет или звонит, что просит, есть ли ссылка, код, деньги или установка приложения. Я помогу спокойно разобрать риск и предложу следующий шаг.`,
    },
  ]);
  const [input, setInput] = useState('');
  const [loading, setLoading] = useState(false);

  useEffect(() => {
    setMessages([
      {
        role: 'assistant',
        content: `Опишите ситуацию для режима “${audience.label}”: кто пишет или звонит, что просит, есть ли ссылка, код, деньги или установка приложения.`,
      },
    ]);
  }, [audience.id]);

  const prompts = {
    senior: ['Мне позвонили из банка и просят код', 'Сказали перевести деньги на безопасный счёт', 'Курьер просит назвать код для доставки'],
    student: ['Мне предложили работу и просят оплатить доступ', 'Продавец прислал внешнюю ссылку на оплату', 'Пишет руководитель и просит код по ссылке'],
    child: ['В игре обещают приз и просят код', 'Незнакомец просит скачать мод по ссылке', 'Администратор игры просит показать экран'],
    relative: ['Маме позвонили и напугали безопасным счётом', 'Как объяснить родителям правило перепроверки', 'Ребёнку в игре обещают бонус и просят телефон родителей'],
    general: ['Мне пишут срочно перевести деньги знакомому', 'Не понимаю, безопасна ли эта ссылка', 'Курьер просит код для вручения заказа'],
  };

  const sendMessage = async (question) => {
    const clean = question.trim();
    if (!clean || loading) return;

    const nextMessages = [...messages, { role: 'user', content: clean }];
    setMessages(nextMessages);
    setInput('');
    setLoading(true);

    try {
      const response = await fetch('/api/gigachat', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          model: 'GigaChat',
          audience: audience.id,
          systemPrompt: [
            'Ты помощник сайта по цифровой безопасности.',
            'Отвечай на русском языке.',
            'Пиши спокойно, коротко и по делу.',
            'Сначала дай конкретные безопасные шаги, потом коротко объясни почему.',
            `Текущий режим: ${audience.label}.`,
          ].join(' '),
          messages: nextMessages,
        }),
      });

      const data = await response.json().catch(() => ({}));

      if (!response.ok) {
        throw new Error([data.error, data.code, data.cause].filter(Boolean).join(' | ') || 'Не удалось получить ответ от GigaChat.');
      }

      const reply =
        data.content ||
        data.answer ||
        data.choices?.[0]?.message?.content ||
        'Сервис вернул пустой ответ. Проверьте подключение GigaChat.';

      setMessages((current) => [...current, { role: 'assistant', content: String(reply) }]);
    } catch (error) {
      setMessages((current) => [
        ...current,
        {
          role: 'assistant',
          content: error.message || 'Не удалось обратиться к GigaChat. Проверьте подключение и попробуйте ещё раз.',
        },
      ]);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="page-stack">
      <SectionTitle
        eyebrow="AI‑чат"
        title={audience.id === "child" ? "Проверить ловушку через GigaChat" : "Разбор подозрительной ситуации через GigaChat"}
        text={audience.id === "child" ? "Здесь можно описать сообщение, игру, приз, ссылку или просьбу прислать код. Чат ответит простыми словами и подскажет безопасный ход." : "Здесь можно описать звонок, сообщение, ссылку или просьбу о деньгах и получить спокойный разбор с безопасными шагами."}
      />

      <div className="layout-2col">
        <aside className="stack-list">
          <article className="card">
            <h3>Что можно спросить</h3>
            <p>Опишите звонок, сообщение, ссылку, просьбу о деньгах или установке приложения. Чат поможет быстро разобрать риск и выбрать безопасный следующий шаг.</p>
            <div className="note-box">Чем подробнее вы опишете ситуацию, тем полезнее будет ответ.</div>
          </article>

          <article className="card">
            <h3>Быстрые вопросы для режима “{audience.label}”</h3>
            <div className="chip-row">
              {prompts[audience.id].map((prompt) => (
                <button key={prompt} type="button" className="chip" onClick={() => sendMessage(prompt)}>
                  {prompt}
                </button>
              ))}
            </div>
          </article>
        </aside>

        <section className="card ai-chat">
          <div className="message-list">
            {messages.map((message, index) => (
              <div key={`${message.role}-${index}`} className={`message-bubble message-bubble--${message.role}`}>
                <strong>{message.role === 'assistant' ? 'GigaChat' : 'Вы'}</strong>
                <p>{message.content}</p>
              </div>
            ))}
            {loading ? <div className="message-bubble message-bubble--assistant"><p>GigaChat отвечает…</p></div> : null}
          </div>
          <form
            className="chat-form"
            onSubmit={(event) => {
              event.preventDefault();
              sendMessage(input);
            }}
          >
            <textarea value={input} onChange={(event) => setInput(event.target.value)} rows={4} placeholder="Опишите ситуацию подробно: кто пишет, что просит, есть ли код, ссылка, деньги или приложение" />
            <button type="submit" className="button" disabled={loading}>Отправить</button>
          </form>
        </section>
      </div>
    </div>
  );
}

